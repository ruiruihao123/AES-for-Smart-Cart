/*******************************************************************************
* 函数名		: gmult(unsigned char a, unsigned char b)
* 函数功能		: 列混合操作中两个字节的乘法运算
* 输入			: 乘数、被乘数
* 输出			: 乘法结果
*******************************************************************************/

unsigned char gmult(unsigned char a, unsigned char b) {
	unsigned char p = 0, i = 0, hbs = 0;
	for (i = 0; i < 8; i++) {
		if (b & 1) {   //b八比特&0000 0001
			p ^= a;
		}
		hbs = a & 0x80;  //看a的首位是否为1 ,扩大2倍是否异或0x1b.
		a <<= 1;
		if (hbs) a = a^(0x1b&0xFF);   //防止SPA
		else a=a^(0x1b&0x00);
		b >>= 1;
	}
	return (unsigned char)p;
}


/*******************************************************************************
* 函数名		: mix_columns(unsigned char *state)
* 函数功能		: 实现列混合操作，将内部矩阵左乘
* 输入			: 16字节的中间值
* 输出			: 无
*******************************************************************************/

