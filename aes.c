/*******************************************************************************
* 函数名		: AES_inv(unsigned char *key, unsigned char *plaintext, unsigned char *ciphertext)
* 函数功能		: 用密钥对密文进行解密
* 输入			: 密文、密钥、明文的指针
* 输出			: 无（明文通过指针返回）
*******************************************************************************/

void AES_Decrypt(unsigned char *key, unsigned char *ciphertext, unsigned char *plaintext){
	int i,r=10;
	unsigned char MatrixShiftMatrix[16]={0,5,10,15,4,9,14,3,8,13,2,7,12,1,6,11};  //矩阵变换+行移位
	key_expansion(key,w);	   //密钥扩展操作

	//倒数第10轮
	//P24=1;
	for (i = 0; i < 16; i ++)
	{
		y[MatrixShiftMatrix[i]]=Sbox_inv[ciphertext[i]^w[i+r*16]];  //密钥加+S盒+行移位
	}
	//倒数9-1轮
	for(r=9;r>0;r--){

		for(i=0;i<16;i++){
			state[i]=y[i]^w[i+r*16];											//将y[i]原值保存到state[i]中,以防y[i]被覆盖
		}
		inv_mix_columns(state);
		for (i = 0; i <= 15; i ++)
		{
			y[MatrixShiftMatrix[i]]=Sbox_inv[state[i]];
		}
	}
	//最后1轮
	for(i=0;i<16;i++)
		plaintext[i]=y[i]^w[i];
}

