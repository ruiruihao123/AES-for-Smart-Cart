/*******************************************************************************
* 函数名		: key_expansion(unsigned char *key, unsigned char *w)
* 函数功能		: 实现密钥扩展操作
* 输入			: 16字节初始密钥，保存1-11轮所有扩展密钥的指针
* 输出			: 无
*******************************************************************************/

void key_expansion(unsigned char *key, unsigned char *w){
	unsigned char tmp[4],tmp_rot[4];
	unsigned char RC[10]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};
	unsigned char Rcon[4]={0x00,0x00,0x00,0x00};
	unsigned char RotWord[4]={3,0,1,2};
	unsigned char i,len=4*11;
	//0-15字节的初始密钥
	for(i=0;i<16;i++){
		w[i]=key[i];
	}
	for(i=4;i<len;i++){
		tmp[0] = w[4*(i-1)+0];
		tmp[1] = w[4*(i-1)+1];
		tmp[2] = w[4*(i-1)+2];
		tmp[3] = w[4*(i-1)+3];
		if(i%4==0){
			Rcon[0]=RC[(i/4)-1];
			//左移位操作
			tmp_rot[RotWord[0]]=tmp[0];	tmp_rot[RotWord[1]]=tmp[1];
			tmp_rot[RotWord[2]]=tmp[2]; tmp_rot[RotWord[3]]=tmp[3];
			//查S盒并与Rcon异或
			tmp[0]=Sbox[tmp_rot[0]]^Rcon[0];
			tmp[1]=Sbox[tmp_rot[1]]^Rcon[1];
			tmp[2]=Sbox[tmp_rot[2]]^Rcon[2];
			tmp[3]=Sbox[tmp_rot[3]]^Rcon[3];
		}
		w[4*i+0] = w[4*(i-4)+0]^tmp[0];
		w[4*i+1] = w[4*(i-4)+1]^tmp[1];
		w[4*i+2] = w[4*(i-4)+2]^tmp[2];
		w[4*i+3] = w[4*(i-4)+3]^tmp[3];
	}
}

